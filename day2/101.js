// 변수 a,b에 각각 숫자를 받아서 더하기 / 나누기 / 곱하기 / 빼기

let a = 5;
let b = 10;

let result = a + b;
console.log(result); // 15

result = a - b;
console.log(result); // -5

result = a / b;
console.log(result); // 0.5

result = a * b;
console.log(result); // 50

// 연산자 - 나머지 연산자 4 / 3 = 몫은 1, 나머지는 1

console.log(5 % 4);

console.log(2 ** 3);

console.clear();
a = a += 1;

console.log(a);

a = a -= 1;
console.log(a);

let c = 1;
let d = ++c;
let e = c--;

